import * as React from "react";
import { cva, type VariantProps } from "class-variance-authority";
import { cn } from "@/lib/utils";
import { Slot } from "@radix-ui/react-slot";

const buttonVariants = cva(
  "inline-flex items-center justify-center whitespace-nowrap rounded-md text-sm font-medium transition-all duration-200 focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-offset-2 disabled:pointer-events-none disabled:opacity-50",
  {
    variants: {
      variant: {
        default:
          "bg-gnome-blue text-white shadow-gnome-sm hover:shadow-gnome hover:brightness-110",
        destructive:
          "bg-gnome-red text-white shadow-gnome-sm hover:shadow-gnome hover:brightness-110",
        outline:
          "border-2 border-gnome-blue bg-transparent text-gnome-blue shadow-gnome-sm hover:shadow-gnome hover:bg-gnome-blue/10",
        secondary:
          "bg-gnome-purple text-white shadow-gnome-sm hover:shadow-gnome hover:brightness-110",
        ghost: "hover:bg-gnome-light dark:hover:bg-gnome-dark/80",
        link: "text-gnome-blue underline-offset-4 hover:underline",
      },
      size: {
        default: "h-10 px-4 py-2",
        sm: "h-8 rounded-md px-3 text-xs",
        lg: "h-12 rounded-md px-8",
        icon: "h-10 w-10",
      },
    },
    defaultVariants: {
      variant: "default",
      size: "default",
    },
  }
);

export interface ButtonProps
  extends React.ButtonHTMLAttributes<HTMLButtonElement>,
    VariantProps<typeof buttonVariants> {
  asChild?: boolean;
}

const Button = React.forwardRef<HTMLButtonElement, ButtonProps>(
  ({ className, variant, size, asChild = false, ...props }, ref) => {
    const Comp = asChild ? Slot : "button";
    return (
      <Comp
        className={cn(buttonVariants({ variant, size, className }))}
        ref={ref}
        {...props}
      />
    );
  }
);
Button.displayName = "Button";

export { Button, buttonVariants };
